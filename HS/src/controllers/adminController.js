// Admin dashboard stats
exports.getDashboardStats = async (req, res) => {
  try {
    const totalDoctors = await Doctor.countDocuments();
    const totalPatients = await Patient.countDocuments();
    const totalAppointments = await Appointment.countDocuments();
    const todayAppointments = await Appointment.countDocuments({
      date: {
        $gte: new Date().setHours(0, 0, 0),
        $lt: new Date().setHours(23, 59, 59)
      }
    });
    
    const revenue = await Appointment.aggregate([
      { $match: { paymentStatus: 'paid' } },
      { $group: { _id: null, total: { $sum: '$amount' } } }
    ]);
    
    res.json({
      success: true,
      stats: {
        totalDoctors,
        totalPatients,
        totalAppointments,
        todayAppointments,
        totalRevenue: revenue[0]?.total || 0
      }
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// Get appointment report
exports.getAppointmentReport = async (req, res) => {
  try {
    const { startDate, endDate } = req.query;
    
    const query = {};
    if (startDate && endDate) {
      query.createdAt = {
        $gte: new Date(startDate),
        $lte: new Date(endDate)
      };
    }
    
    const appointments = await Appointment.find(query)
      .populate('patientId', 'name email')
      .populate('doctorId', 'specialization');
    
    const completed = appointments.filter(a => a.status === 'completed').length;
    const cancelled = appointments.filter(a => a.status === 'cancelled').length;
    const pending = appointments.filter(a => a.status === 'pending').length;
    
    res.json({
      success: true,
      report: {
        total: appointments.length,
        completed,
        cancelled,
        pending,
        appointments
      }
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// Get revenue report
exports.getRevenueReport = async (req, res) => {
  try {
    const { startDate, endDate } = req.query;
    
    const query = { paymentStatus: 'paid' };
    if (startDate && endDate) {
      query.createdAt = {
        $gte: new Date(startDate),
        $lte: new Date(endDate)
      };
    }
    
    const appointments = await Appointment.find(query);
    const totalRevenue = appointments.reduce((sum, app) => sum + (app.amount || 0), 0);
    
    const monthlyRevenue = await Appointment.aggregate([
      { $match: query },
      {
        $group: {
          _id: { $month: '$createdAt' },
          total: { $sum: '$amount' },
          count: { $sum: 1 }
        }
      }
    ]);
    
    res.json({
      success: true,
      report: {
        totalRevenue,
        totalAppointments: appointments.length,
        monthlyRevenue
      }
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// Get doctor performance report
exports.getDoctorPerformanceReport = async (req, res) => {
  try {
    const doctors = await Doctor.find().populate('userId', 'name');
    
    const performance = await Promise.all(doctors.map(async (doctor) => {
      const appointments = await Appointment.find({ doctorId: doctor._id });
      const completed = appointments.filter(a => a.status === 'completed').length;
      const totalRevenue = appointments
        .filter(a => a.paymentStatus === 'paid')
        .reduce((sum, a) => sum + (a.amount || 0), 0);
      
      return {
        doctorId: doctor._id,
        name: doctor.userId?.name,
        specialization: doctor.specialization,
        totalAppointments: appointments.length,
        completedAppointments: completed,
        totalRevenue,
        rating: doctor.rating
      };
    }));
    
    res.json({ success: true, performance });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

