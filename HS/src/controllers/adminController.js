// Create doctor with auto-generated password
exports.createDoctor = async (req, res) => {
  try {
    const {
      name, email, phone, specialization, qualification,
      experience, consultationFee, treatsSymptoms, bio, clinicAddress
    } = req.body;
    
    // Check if user already exists
    let existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({ success: false, message: 'Email already exists' });
    }
    
    // Generate password and user ID
    const password = generateRandomPassword(10);
    const userId = generateUserId(name, 'doctor');
    
    // Create user
    const user = await User.create({
      name,
      email,
      password,
      phone,
      role: 'doctor',
      isActive: true
    });
    
    // Create doctor profile
    const doctor = await Doctor.create({
      userId: user._id,
      specialization,
      qualification,
      experience: experience || 0,
      consultationFee,
      treatsSymptoms: treatsSymptoms || [],
      bio: bio || '',
      clinicAddress: clinicAddress || ''
    });
    
    // Send credentials via email
    await emailService.sendDoctorCredentials(email, name, user._id, password);
    
    res.status(201).json({
      success: true,
      message: 'Doctor created successfully. Credentials sent to email.',
      data: {
        doctorId: doctor._id,
        userId: user._id,
        name: user.name,
        email: user.email,
        specialization: doctor.specialization,
        consultationFee: doctor.consultationFee,
        autoGeneratedPassword: password // Remove in production
      }
    });
    
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

